package com.dn.shop.model.dto.product;

import java.math.BigDecimal;

public class ProductDTO {
    private Long id; // Unique identifier of the product
    private String name; // Product name
    private String description; // Product description
    private BigDecimal price; // Product price
    private int stock; // Available stock
    private String categoryName; // The name of the category this product belongs to

    // Getters and Setters
    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public BigDecimal getPrice() {
        return price;
    }

    public void setPrice(BigDecimal price) {
        this.price = price;
    }

    public int getStock() {
        return stock;
    }

    public void setStock(int stock) {
        this.stock = stock;
    }

    public String getCategoryName() {
        return categoryName;
    }

    public void setCategoryName(String categoryName) {
        this.categoryName = categoryName;
    }
}

